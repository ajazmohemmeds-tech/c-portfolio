// ── Local Rule-Based Engine ─────────────────────────────────────────────────
const generateLocalResponse = (input) => {
  const text = input.toLowerCase();
  
  // Greetings
  if (text.match(/\b(hi|hello|hey|greetings|morning|afternoon)\b/)) {
    return "Hello! 👋 I'm Kaizen, Ajaz's personal AI representative. I can tell you all about his expertise in Generative AI, his NORA architecture, diffusion research, and technical skills. What would you like to explore?";
  }

  // Identity / Who is he?
  if (text.match(/who is|about (ajaz|him)|tell me about ajaz/)) {
    return KNOWLEDGE.identity;
  }

  // NORA Specific
  if (text.match(/nora|residential assistant|neural operated/)) {
    return KNOWLEDGE.nora;
  }

  // Research (Diffusion / SCIS 2025)
  if (text.match(/diffusion|scis|transformer|word impact|publish|paper|text-to-image/)) {
    return KNOWLEDGE.research;
  }

  // Ongoing Research (Secure Voice AI)
  if (text.match(/ongoing|current research|secure voice|offline-first|privacy|multimodal/)) {
    return KNOWLEDGE.ongoing_research;
  }

  // Education
  if (text.match(/education|study|studied|degree|college|university|school/)) {
    return KNOWLEDGE.education;
  }

  // Experience / Internships
  if (text.match(/experience|work|intern|internship|cognifyz|trizlabs/)) {
    return KNOWLEDGE.experience;
  }

  // Projects General
  if (text.match(/project|build|built|business intelligence|bi agent|monday\.com/)) {
    return KNOWLEDGE.projects;
  }

  // Skills & Technologies
  if (text.match(/skill|tech|programming|language|framework|python|react|flask|api|llm/)) {
    return KNOWLEDGE.skills;
  }

  // Certifications
  if (text.match(/cert|nptel|aws|cisco|certificate/)) {
    return KNOWLEDGE.certifications;
  }

  // Volunteering & Extracurriculars
  if (text.match(/volunteer|nss|cuestic|extra|club|leadership/)) {
    return KNOWLEDGE.volunteering;
  }

  // Contact / Collaboration / Recruiters
  if (text.match(/contact|phone|email|number|call|reach|hire|collaborate|recruit/)) {
    return KNOWLEDGE.contact;
  }

  // Fallback
  return "That's a great question! While I can't generate a dynamic answer for that right now, I highly recommend asking me to Explain the NORA project, tell you about his Diffusion Research, or list his AI System Design skills!";
};

const KaizenAssistant = () => {
  const [isOpen, setIsOpen] = useState(false);
  const [messages, setMessages] = useState([
    { role: 'assistant', text: "Hello! 👋 I'm Kaizen, Ajaz's personal AI representative. I'm here to help visitors, recruiters, and collaborators learn about his AI projects, diffusion research, and engineering skills. How can I help you today?", type: 'message' }
  ]);
  const [input, setInput] = useState('');
  const [isThinking, setIsThinking] = useState(false);
  const [thoughtSteps, setThoughtSteps] = useState([]);
  const messagesEndRef = useRef(null);
  
  // Retaining the original icon variable but using NORA context
  const kaizenIcon = `${import.meta.env.BASE_URL}images/kaizen_icon.png`;

  useEffect(() => {
    messagesEndRef.current?.scrollIntoView({ behavior: "smooth" });
  }, [messages, thoughtSteps]);

  const handleSend = async (text) => {
    if (!text.trim() || isThinking) return;

    setMessages(prev => [...prev, { role: 'user', text, type: 'message' }]);
    setInput('');
    setIsThinking(true);
    setThoughtSteps([
      { id: 1, label: "Analyzing intent...", status: "pending" },
      { id: 2, label: "Scanning portfolio knowledge...", status: "pending" },
      { id: 3, label: "Synthesizing response...", status: "pending" }
    ]);

    // Simulate thinking time for a natural conversational flow
    setTimeout(() => {
      setThoughtSteps(prev => prev.map(s => s.id === 1 ? { ...s, status: 'complete' } : s));
    }, 400);

    setTimeout(() => {
      setThoughtSteps(prev => prev.map(s => s.id === 2 ? { ...s, status: 'complete' } : s));
    }, 900);

    setTimeout(() => {
      const responseText = generateLocalResponse(text);
      setThoughtSteps(prev => prev.map(s => s.id === 3 ? { ...s, status: 'complete' } : s));
      
      setTimeout(() => {
        setMessages(prev => [...prev, { role: 'assistant', text: responseText, type: 'message' }]);
        setIsThinking(false);
        setThoughtSteps([]);
      }, 400);
    }, 1400);
  };

  return (
    <div className={`kaizen-widget ${isOpen ? 'open' : ''}`}>
      {!isOpen && (
        <button className="kaizen-trigger" onClick={() => setIsOpen(true)}>
          <img src={kaizenIcon} alt="Kaizen" className="kaizen-icon-img brain-icon" />
          <span>Ask Kaizen</span>
        </button>
      )}
      {isOpen && (
        <div className="kaizen-chat-window">
          <div className="kaizen-header">
            <div className="kaizen-title">
              <img src={kaizenIcon} alt="Kaizen" className="kaizen-icon-img small" />
              <span>Ask Kaizen</span>
            </div>
            <button className="kaizen-close" onClick={() => setIsOpen(false)}><X size={20} /></button>
          </div>

          <div className="kaizen-messages">
            {messages.map((msg, i) => (
              <div key={i} className={`message-bubble ${msg.role}`}>
                <div className="bubble-text" style={{ whiteSpace: 'pre-wrap' }}>{msg.text}</div>
              </div>
            ))}
            {isThinking && (
              <div className="thought-process">
                <div className="thought-header">
                  <div className="thinking-dots"><span></span><span></span><span></span></div>
                  <span>Kaizen is thinking...</span>
                </div>
                <div className="thought-steps">
                  {thoughtSteps.map(step => (
                    <div key={step.id} className={`thought-step ${step.status}`}>
                      {step.status === 'complete' ? <CheckCircle2 size={12} /> : <ChevronRight size={12} />}
                      <span>{step.label}</span>
                    </div>
                  ))}
                </div>
              </div>
            )}
            <div ref={messagesEndRef} />
          </div>

          <div className="kaizen-quick-prompts">
            <button onClick={() => handleSend("Explain the NORA project")}>NORA Architecture</button>
            <button onClick={() => handleSend("Explain Ajaz's diffusion research")}>Diffusion Research</button>
            <button onClick={() => handleSend("What research is Ajaz currently working on?")}>Current Research</button>
            <button onClick={() => handleSend("What technologies does Ajaz use?")}>Tech Stack</button>
            <button onClick={() => handleSend("How can I contact Ajaz?")}>Contact</button>
          </div>

          <form className="kaizen-input-area" onSubmit={(e) => { e.preventDefault(); handleSend(input); }}>
            <input
              type="text"
              placeholder="Ask Kaizen about Ajaz..."
              value={input}
              onChange={(e) => setInput(e.target.value)}
            />
            <button type="submit" disabled={!input.trim() || isThinking}><Send size={18} /></button>
          </form>
        </div>
      )}
    </div>
  );
};

export default KaizenAssistant;
